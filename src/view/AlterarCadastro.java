    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        // TODO add your handling code here:
        
        
        if(jTextFieldID.getText().equals("") || new String(jPasswordFieldSenha.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Os campos devem ser preenchidos");  
        }else{
            user.setId(Integer.valueOf(jTextFieldID.getText()));
            control.TrazerUsuario(user);
            if(user.getSenhaUsuario().equals(new String(jPasswordFieldSenha.getPassword())) ){
                jTextFieldNome.setText(user.getNomeUsuario());
                jTextFieldNasc.setText(user.getDatanascUsuario());
            }else{            
                JOptionPane.showMessageDialog(rootPane, "Senha ou Usuario Invalidos");
            }
        }
        
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

