    private void jButAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAlterarActionPerformed
        // TODO add your handling code here:
      
        
        if(jTextFieldNome.getText().equals("") || jTextFieldNasc.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Os campos devem ser preenchidos");
        }else{
            if(jTextFieldNome.getText().length() <= 45){
                if(String.valueOf(jTextFieldNasc.getText().charAt(4)).equals("-")
                && String.valueOf(jTextFieldNasc.getText().charAt(7)).equals("-")
                && jTextFieldNasc.getText().length() == 10){
                    user.setDatanascUsuario(jTextFieldNasc.getText());
                    user.setNomeUsuario(jTextFieldNome.getText());
                    control.AlterarCadastro(user);
                    jTextFieldNasc.setEnabled(!true);
                    jTextFieldNome.setEnabled(!true);
                    jTextFieldNasc.setText("");
                    jTextFieldNome.setText("");
                    jTextFieldID.setText("");
                    jPasswordFieldSenha.setText("");
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Data de Nascimento deve ter\no formato AAAA/MM/DD");
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Nome Deve Conter No Maximo 45 Caracteres");
            }
        }
    }//GEN-LAST:event_jButAlterarActionPerformed

